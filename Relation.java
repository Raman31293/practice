public class Relation {
    public static void main(String[] args) {
        int num1 = 10 ; int num2 = 20;
        if (num1 == num2){
            System.out.println("Equal : True");
        }
        else{
            System.out.println("Equale : False");
        }
        if (num1 != num2){
            System.out.println("Not Equal : True");
        }
        else{
            System.out.println("Not Equale : False");
        }
        if (num1 >= num2){
            System.out.println("Num1 is greater than Num2 : True");
        }
        else{
            System.out.println("Num1 is greater than Num2 : False");
        }
        if (num1 <= num2){
            System.out.println("Num2 is greater than Num1 : True");
        }
        else{
            System.out.println("Num2 is greater than Num1 : False");
        }
        
    }      
}
